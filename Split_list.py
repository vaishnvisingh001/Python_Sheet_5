s="Hello everyone how are you"
print(s.split())                #['Hello', 'everyone', 'how', 'are', 'you']
s="Hello-everyone-how are you"
print(s.split("-"))             #['Hello', 'everyone', 'how are you']
word='Suyash:Chaudhary:Noida'
print(word.split(':'))          #['Suyash', 'Chaudhary', 'Noida']
t="23456"
print(t.split())                #['23456']
t="2345"
print(t.split())                #['2345']
